/**
* @desc      Customer registration form host window
* @author    Wondwosen Desalegn
* @copyright (c) 2017, Cybersoft
* @date      November 01, 2017
* @namespace Ext.erp.iffs.ux.customer
* @class     Ext.erp.iffs.ux.customer.Window
* @extends   Ext.Window
*/
Ext.erp.iffs.ux.customer.Window = function (config) {
    Ext.erp.iffs.ux.customer.Window.superclass.constructor.call(this, Ext.apply({
        layout: 'fit',
        width: 500,
        autoHeight: true,
        closeAction: 'close',
        modal: true,
        resizable: false,
        buttonAlign: 'right',
        bodyStyle: 'padding:5px;',
        listeners: {
            show: function () {
                this.form.getForm().findField('Id').setValue(this.customerId);
                if (this.customerId != '') {
                    this.form.load({ params: { id: this.customerId } });
                }
            },
            scope: this
        }
    }, config));
};
Ext.extend(Ext.erp.iffs.ux.customer.Window, Ext.Window, {
    initComponent: function () {
        this.form = new Ext.erp.iffs.ux.customer.Form({targetForm:this.targetForm});
        this.items = [this.form];
        this.buttons = [{
            text: 'Save',
            iconCls: 'icon-save',
            handler: this.onSave,
            scope: this
        }, {
            xtype: 'tbseparator'
        }, {
            text: 'Close',
            iconCls: 'icon-exit',
            handler: this.onClose,
            scope: this
        }];
        this.tools = [{
            id: 'refresh',
            qtip: 'Reset',
            handler: function () {
                this.form.getForm().reset();
            },
            scope: this
        }];
        Ext.erp.iffs.ux.customer.Window.superclass.initComponent.call(this, arguments);
    },
    onSave: function () {
        if (!this.form.getForm().isValid()) return;
        this.form.getForm().submit({
            waitMsg: 'Please wait...',
            success: function (form, action) {
                if (typeof form.targetForm != "undefined" && form.targetForm != null) {
                    form.targetForm.findField('CustomerId').setValue(action.result.customerId);
                    form.targetForm.findField('Customer').setValue(Ext.getCmp('customer-form').getForm().findField('Name').getValue());
                    form.targetForm.findField('CustomerGroupId').setValue(Ext.getCmp('customer-form').getForm().findField('CustomerGroupId').getValue());
                    form.targetForm.findField('CustomerGroup').setValue(Ext.getCmp('customer-form').getForm().findField('CustomerGroupId').getRawValue());
                    Ext.MessageBox.show({
                        title: 'Sucess',
                        msg: action.result.data,
                        buttons: Ext.Msg.OK,
                        icon: Ext.MessageBox.SUCCESS,
                        scope: this
                    });
                }
                Ext.getCmp('customer-form').getForm().reset();
                Ext.getCmp('customer-paging').doRefresh();
            },
            failure: function (form, action) {
                Ext.MessageBox.show({
                    title: 'Error',
                    msg: action.result.data,
                    buttons: Ext.Msg.OK,
                    icon: Ext.MessageBox.ERROR,
                    scope: this
                });
            }
        });
    },
    onClose: function () {
        this.close();
    }
});
Ext.reg('customer-window', Ext.erp.iffs.ux.customer.Window);

